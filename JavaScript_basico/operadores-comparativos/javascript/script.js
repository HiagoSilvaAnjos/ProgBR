// == igual a (Compara para ver se o valor é igual)
console.log(3 == 3); // Neste caso seria verdadeiro pois 3 é igual a 3.

// === valor igual e igual (compara para ver se o valor e o tipo são iguais)
console.log("3" === 3); // Neste caso seria falso pois "3" é uma string e 3 é número.

// != não é igual (Compara os valores para ver se são diferentes)
console.log(3 != 4); // Neste caso seria verdadeiro pois 3 é diferente de 4

// !== não é igual ou não igual (compara para ver se o valor e o tipo são diferentes)
console.log(3 !== 3); // Neste caso seria falso pois 3 não é diferente de 3

// > maior que (Compara para ver se um número é maior que outro)
console.log(3 > 2); // Neste caso seria verdadeiro pois 3 é maior que 2

// < menor de (Compara para ver se um número é menor que outro)
console.log(3 < 2); // Neste caso seria falso pois 3 não é menor que 2

// >= maior que ou igual a (Compara os valores para ver se ele é igual ou maior que o outro valor)
console.log(3 >= 3) // Neste caso seria verdadeiro pois 3 não é maior que 3 porem ele é igual

// <= menor ou igual a (Compara os valores para ver se ele é igual ou menor que o outro valor)
console.log(3 <= 2) // Neste caso seria falso pois 3 não é menor e nem igual a 2