function sumirH1() {
    let h = document.getElementById('h1');
    h.style.display = 'none';
    
}

function aparecerH1() {
    let h1 = document.getElementById('h1');
    h1.style.display = 'block';
}

function mudarfont() {
    let h1 = document.getElementById('h1');
    h1.style.fontFamily = 'Arial';
}

function voltarfont() {
    let h1 = document.getElementById('h1');
    h1.style.fontFamily = 'serif';
}